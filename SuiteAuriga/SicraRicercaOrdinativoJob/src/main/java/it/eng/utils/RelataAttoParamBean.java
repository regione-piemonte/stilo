public class RelataAttoParamBean {
	
	private String idUd;
	private String nomeModello;
	private String uriModello;
	private String tipoModello;
	private String tipoTimbro;
	
	public String getIdUd() {
		return idUd;
	}
	public String getNomeModello() {
		return nomeModello;
	}
	public String getUriModello() {
		return uriModello;
	}
	public String getTipoModello() {
		return tipoModello;
	}
	public String getTipoTimbro() {
		return tipoTimbro;
	}
	public void setIdUd(String idUd) {
		this.idUd = idUd;
	}
	public void setNomeModello(String nomeModello) {
		this.nomeModello = nomeModello;
	}
	public void setUriModello(String uriModello) {
		this.uriModello = uriModello;
	}
	public void setTipoModello(String tipoModello) {
		this.tipoModello = tipoModello;
	}
	public void setTipoTimbro(String tipoTimbro) {
		this.tipoTimbro = tipoTimbro;
	}
	
	
	
}
