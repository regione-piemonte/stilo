public class DirigenteDiConcertoBean {
	
	private String dirigenteConcerto;
	private String dirigenteConcertoFromLoadDett;
	private String codUoDirigenteConcerto;
	private String desDirigenteConcerto;
	private Boolean flgDirigenteConcertoFirmatario;
	
	public String getDirigenteConcerto() {
		return dirigenteConcerto;
	}
	public void setDirigenteConcerto(String dirigenteConcerto) {
		this.dirigenteConcerto = dirigenteConcerto;
	}
	public String getDirigenteConcertoFromLoadDett() {
		return dirigenteConcertoFromLoadDett;
	}
	public void setDirigenteConcertoFromLoadDett(String dirigenteConcertoFromLoadDett) {
		this.dirigenteConcertoFromLoadDett = dirigenteConcertoFromLoadDett;
	}
	public String getCodUoDirigenteConcerto() {
		return codUoDirigenteConcerto;
	}
	public void setCodUoDirigenteConcerto(String codUoDirigenteConcerto) {
		this.codUoDirigenteConcerto = codUoDirigenteConcerto;
	}
	public String getDesDirigenteConcerto() {
		return desDirigenteConcerto;
	}
	public void setDesDirigenteConcerto(String desDirigenteConcerto) {
		this.desDirigenteConcerto = desDirigenteConcerto;
	}
	public Boolean getFlgDirigenteConcertoFirmatario() {
		return flgDirigenteConcertoFirmatario;
	}
	public void setFlgDirigenteConcertoFirmatario(Boolean flgDirigenteConcertoFirmatario) {
		this.flgDirigenteConcertoFirmatario = flgDirigenteConcertoFirmatario;
	}
	
}
