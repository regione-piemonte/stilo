public class AttoRiferimentoBean {

	private String flgPresenteASistema;
	private String idUd;
	private String categoriaReg;
	private String sigla;
	private String numero;
	private String anno;
	
	public String getFlgPresenteASistema() {
		return flgPresenteASistema;
	}
	public void setFlgPresenteASistema(String flgPresenteASistema) {
		this.flgPresenteASistema = flgPresenteASistema;
	}
	public String getIdUd() {
		return idUd;
	}
	public void setIdUd(String idUd) {
		this.idUd = idUd;
	}
	public String getCategoriaReg() {
		return categoriaReg;
	}
	public void setCategoriaReg(String categoriaReg) {
		this.categoriaReg = categoriaReg;
	}
	public String getSigla() {
		return sigla;
	}
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getAnno() {
		return anno;
	}
	public void setAnno(String anno) {
		this.anno = anno;
	}
	
}
