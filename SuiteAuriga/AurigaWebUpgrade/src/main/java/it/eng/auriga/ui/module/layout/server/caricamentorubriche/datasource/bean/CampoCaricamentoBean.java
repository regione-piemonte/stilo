public class CampoCaricamentoBean {

	private String nomeCampo;

	private String colonnaRif;

	private String type;

	/**
	 * @return the nomeCampo
	 */
	public String getNomeCampo() {
		return nomeCampo;
	}

	/**
	 * @param nomeCampo
	 *            the nomeCampo to set
	 */
	public void setNomeCampo(String nomeCampo) {
		this.nomeCampo = nomeCampo;
	}

	/**
	 * @return the colonnaRif
	 */
	public String getColonnaRif() {
		return colonnaRif;
	}

	/**
	 * @param colonnaRif
	 *            the colonnaRif to set
	 */
	public void setColonnaRif(String colonnaRif) {
		this.colonnaRif = colonnaRif;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

}
