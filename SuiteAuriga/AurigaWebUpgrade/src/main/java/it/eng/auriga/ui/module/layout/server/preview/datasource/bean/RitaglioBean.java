public class RitaglioBean {
	
	private float llx;
	
	private float lly;
	
	private float urx;
	
	private float ury;
	
	private String color;
	
	private String rotation;

	public float getLlx() {
		return llx;
	}

	public void setLlx(float llx) {
		this.llx = llx;
	}

	public float getLly() {
		return lly;
	}

	public void setLly(float lly) {
		this.lly = lly;
	}

	public float getUrx() {
		return urx;
	}

	public void setUrx(float urx) {
		this.urx = urx;
	}

	public float getUry() {
		return ury;
	}

	public void setUry(float ury) {
		this.ury = ury;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getRotation() {
		return rotation;
	}

	public void setRotation(String rotation) {
		this.rotation = rotation;
	}

	

	
}
