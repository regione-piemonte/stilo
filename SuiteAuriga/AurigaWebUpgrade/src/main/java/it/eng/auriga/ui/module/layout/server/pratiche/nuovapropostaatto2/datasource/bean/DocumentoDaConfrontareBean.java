public class DocumentoDaConfrontareBean {

	private String uriFile;
	private String nomeFile;
	private String mimetypeFile;
	private String descrizioneFile;

	public String getUriFile() {
		return uriFile;
	}

	public void setUriFile(String uriFile) {
		this.uriFile = uriFile;
	}

	public String getNomeFile() {
		return nomeFile;
	}

	public void setNomeFile(String nomeFile) {
		this.nomeFile = nomeFile;
	}

	public String getMimetypeFile() {
		return mimetypeFile;
	}

	public void setMimetypeFile(String mimetypeFile) {
		this.mimetypeFile = mimetypeFile;
	}

	public String getDescrizioneFile() {
		return descrizioneFile;
	}

	public void setDescrizioneFile(String descrizioneFile) {
		this.descrizioneFile = descrizioneFile;
	}

}
