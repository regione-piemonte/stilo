public class FruitoreCasellaBean {
	
	private String tipo;
	private String codRapido;
	private String idFruitoreCasella;
	private String denominazione;
	private String idSpAoo;
	private Boolean flgSmistatore;
	private Boolean flgMittente;
	private Boolean flgAmministratore;
	private Boolean flgIncludiSubordinati;
	
	
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}	
	public String getCodRapido() {
		return codRapido;
	}
	public void setCodRapido(String codRapido) {
		this.codRapido = codRapido;
	}
	public String getIdFruitoreCasella() {
		return idFruitoreCasella;
	}
	public void setIdFruitoreCasella(String idFruitoreCasella) {
		this.idFruitoreCasella = idFruitoreCasella;
	}	
	public String getDenominazione() {
		return denominazione;
	}
	public void setDenominazione(String denominazione) {
		this.denominazione = denominazione;
	}
	public String getIdSpAoo() {
		return idSpAoo;
	}
	public void setIdSpAoo(String idSpAoo) {
		this.idSpAoo = idSpAoo;
	}
	public Boolean getFlgSmistatore() {
		return flgSmistatore;
	}
	public void setFlgSmistatore(Boolean flgSmistatore) {
		this.flgSmistatore = flgSmistatore;
	}
	public Boolean getFlgMittente() {
		return flgMittente;
	}
	public void setFlgMittente(Boolean flgMittente) {
		this.flgMittente = flgMittente;
	}
	public Boolean getFlgAmministratore() {
		return flgAmministratore;
	}
	public void setFlgAmministratore(Boolean flgAmministratore) {
		this.flgAmministratore = flgAmministratore;
	}
	public Boolean getFlgIncludiSubordinati() {
		return flgIncludiSubordinati;
	}
	public void setFlgIncludiSubordinati(Boolean flgIncludiSubordinati) {
		this.flgIncludiSubordinati = flgIncludiSubordinati;
	}
	
}

