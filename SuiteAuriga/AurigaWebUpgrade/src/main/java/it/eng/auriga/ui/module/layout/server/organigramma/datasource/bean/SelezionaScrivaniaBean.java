public class SelezionaScrivaniaBean {

	private String idScrivania;
	private String codUoScrivania;
	private String organigramma;
	private String organigrammaFromLoadDett;
	private String descrizione;
	private String descrizioneEstesa;

	public String getIdScrivania() {
		return idScrivania;
	}

	public void setIdScrivania(String idScrivania) {
		this.idScrivania = idScrivania;
	}

	public String getCodUoScrivania() {
		return codUoScrivania;
	}

	public void setCodUoScrivania(String codUoScrivania) {
		this.codUoScrivania = codUoScrivania;
	}

	public String getOrganigramma() {
		return organigramma;
	}

	public void setOrganigramma(String organigramma) {
		this.organigramma = organigramma;
	}

	public String getOrganigrammaFromLoadDett() {
		return organigrammaFromLoadDett;
	}

	public void setOrganigrammaFromLoadDett(String organigrammaFromLoadDett) {
		this.organigrammaFromLoadDett = organigrammaFromLoadDett;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public String getDescrizioneEstesa() {
		return descrizioneEstesa;
	}

	public void setDescrizioneEstesa(String descrizioneEstesa) {
		this.descrizioneEstesa = descrizioneEstesa;
	}

}
