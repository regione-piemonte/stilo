public class ScrivaniaProponentiBean {

	private String idScrivania;
	private String idScrivaniaFromLoadDett;
	private String codUoScrivania;
	private String desScrivania;
	private String tipoVistoScrivania;
	private Boolean flgForzaDispFirmaScrivania;
	
	public String getIdScrivania() {
		return idScrivania;
	}
	public void setIdScrivania(String idScrivania) {
		this.idScrivania = idScrivania;
	}
	public String getIdScrivaniaFromLoadDett() {
		return idScrivaniaFromLoadDett;
	}
	public void setIdScrivaniaFromLoadDett(String idScrivaniaFromLoadDett) {
		this.idScrivaniaFromLoadDett = idScrivaniaFromLoadDett;
	}
	public String getCodUoScrivania() {
		return codUoScrivania;
	}
	public void setCodUoScrivania(String codUoScrivania) {
		this.codUoScrivania = codUoScrivania;
	}
	public String getDesScrivania() {
		return desScrivania;
	}
	public void setDesScrivania(String desScrivania) {
		this.desScrivania = desScrivania;
	}
	public String getTipoVistoScrivania() {
		return tipoVistoScrivania;
	}
	public void setTipoVistoScrivania(String tipoVistoScrivania) {
		this.tipoVistoScrivania = tipoVistoScrivania;
	}
	public Boolean getFlgForzaDispFirmaScrivania() {
		return flgForzaDispFirmaScrivania;
	}
	public void setFlgForzaDispFirmaScrivania(Boolean flgForzaDispFirmaScrivania) {
		this.flgForzaDispFirmaScrivania = flgForzaDispFirmaScrivania;
	}
	
}