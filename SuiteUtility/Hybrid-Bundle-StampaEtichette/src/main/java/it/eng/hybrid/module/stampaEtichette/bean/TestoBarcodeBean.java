package it.eng.hybrid.module.stampaEtichette.bean;

public class TestoBarcodeBean {

	private String testo;
	private String barcode;
	private String testoBarcode;
	private String testoRepertorio;
	private String testoFaldone;
	private String barcodeFaldone;
	private int counter;
	
	public String getTesto() {
		return testo;
	}
	public void setTesto(String testo) {
		this.testo = testo;
	}
	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}
	public String getTestoBarcode() {
		return testoBarcode;
	}
	public void setTestoBarcode(String testoBarcode) {
		this.testoBarcode = testoBarcode;
	}	
	public String getTestoRepertorio() {
		return testoRepertorio;
	}
	public void setTestoRepertorio(String testoRepertorio) {
		this.testoRepertorio = testoRepertorio;
	}
	public int getCounter() {
		return counter;
	}
	public void setCounter(int counter) {
		this.counter = counter;
	}
	public String getTestoFaldone() {
		return testoFaldone;
	}
	public void setTestoFaldone(String testoFaldone) {
		this.testoFaldone = testoFaldone;
	}
	public String getBarcodeFaldone() {
		return barcodeFaldone;
	}
	public void setBarcodeFaldone(String barcodeFaldone) {
		this.barcodeFaldone = barcodeFaldone;
	}
	
}
