package it.eng.dm.sira.service.util;

public class ExternalApp {

	private String urlRelazioni;

	private String urlWizard;

	private String urlWizardProcedimenti;
	
	private String urlFlussoSemplificato;

	public String getUrlRelazioni() {
		return urlRelazioni;
	}

	public void setUrlRelazioni(String urlRelazioni) {
		this.urlRelazioni = urlRelazioni;
	}

	public String getUrlWizard() {
		return urlWizard;
	}

	public void setUrlWizard(String urlWizard) {
		this.urlWizard = urlWizard;
	}

	public String getUrlWizardProcedimenti() {
		return urlWizardProcedimenti;
	}

	public void setUrlWizardProcedimenti(String urlWizardProcedimenti) {
		this.urlWizardProcedimenti = urlWizardProcedimenti;
	}

	public String getUrlFlussoSemplificato() {
		return urlFlussoSemplificato;
	}

	public void setUrlFlussoSemplificato(String urlFlussoSemplificato) {
		this.urlFlussoSemplificato = urlFlussoSemplificato;
	}
}
