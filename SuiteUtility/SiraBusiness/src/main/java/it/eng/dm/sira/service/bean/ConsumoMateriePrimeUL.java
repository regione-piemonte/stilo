package it.eng.dm.sira.service.bean;

public class ConsumoMateriePrimeUL {

	private Float consumoAnnuo;

	private String descrizione;

	private String produttore;

	private String schedaTecnica;

	private String tipo;

	private String unitaMisura;

	public Float getConsumoAnnuo() {
		return consumoAnnuo;
	}

	public void setConsumoAnnuo(Float consumoAnnuo) {
		this.consumoAnnuo = consumoAnnuo;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public String getProduttore() {
		return produttore;
	}

	public void setProduttore(String produttore) {
		this.produttore = produttore;
	}

	public String getSchedaTecnica() {
		return schedaTecnica;
	}

	public void setSchedaTecnica(String schedaTecnica) {
		this.schedaTecnica = schedaTecnica;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getUnitaMisura() {
		return unitaMisura;
	}

	public void setUnitaMisura(String unitaMisura) {
		this.unitaMisura = unitaMisura;
	}

}
