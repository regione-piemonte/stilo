import java.io.File;

public class RiquadroFirmaGrafica {

	private int coordinataXVertice;
	private int coordinataYVertice;
	private int larghezza;
	private int altezza;
	private File fileImmagine;
	
	public int getCoordinataXVertice() {
		return coordinataXVertice;
	}
	public void setCoordinataXVertice(int coordinataXVertice) {
		this.coordinataXVertice = coordinataXVertice;
	}
	public int getCoordinataYVertice() {
		return coordinataYVertice;
	}
	public void setCoordinataYVertice(int coordinataYVertice) {
		this.coordinataYVertice = coordinataYVertice;
	}
	public int getLarghezza() {
		return larghezza;
	}
	public void setLarghezza(int larghezza) {
		this.larghezza = larghezza;
	}
	public int getAltezza() {
		return altezza;
	}
	public void setAltezza(int altezza) {
		this.altezza = altezza;
	}
	public File getFileImmagine() {
		return fileImmagine;
	}
	public void setFileImmagine(File fileImmagine) {
		this.fileImmagine = fileImmagine;
	}
	
	
	
	
	
}
